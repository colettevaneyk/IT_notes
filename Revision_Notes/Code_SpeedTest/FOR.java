package Revision_Notes.Code_SpeedTest;

public class FOR {
   public static void main(String[] args){
      // for loops are more rigid that while loops.
      // these allow us to run code a specific amount of times
      // where whiles allow us to run according to a condition.
      for(int x = 0; x < 5; x++){
         System.out.println("FOR LOOP: " + x);
         //the incrementing happens after the code
      }
   }
}